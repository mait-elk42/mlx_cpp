#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	slen;
	char	*res;

	i = 0;
	if (!s || !f)
		return (0);
	slen = ft_strlen(s);
	res = malloc(slen + 1);
	if (!res)
		return (0);
	while (i < slen)
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
